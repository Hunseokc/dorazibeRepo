package ThisIsJava.ch04;

public class IfelseExam {
    public static void main(String[] args) {
        int score= 85;

        if (score >= 90) {
            System.out.println("점수가 90점 이상\n등급이 A입니다");
        }
        else {
            System.out.println("점수가 90점 미만\n등급이 B입니다");
        }
    }
}
